#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (!dst && !src)
		return (0);
	while (i < n)
	{
		((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
		i++;
	}
	return (dst);
}
/* like cpoystr ft
 * cpy src to dst follow by n and return dst
 * value dst will be the same as src
 * overlap might happen and no protection
 * unsigned char is only positive char
 */
